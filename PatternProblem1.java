public class PatternProblem1 {
    public static void main(String[] args){
        for (int i = 1; i<=4;i++){ //Outer Loop for rows
            for(int j=1;j<=4;j++){ // inner loops for column
                System.out.print("*");
            }
            System.out.println();
            
        }
        
    }
    
}
